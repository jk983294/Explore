#!/usr/bin/perl
use strict;
use warnings;

my $a = 5;
my $b = 2;
print 'Value of $a + $b = ' .  ( $a + $b ) . "\n";    # 7
print 'Value of $a - $b = ' .  ( $a - $b ) . "\n";    # 3
print 'Value of $a * $b = ' .  ( $a * $b ) . "\n";    # 10
print 'Value of $a / $b = ' .  ( $a / $b ) . "\n";    # 2.5
print 'Value of $a % $b = ' .  ( $a % $b ) . "\n";    # 1
print 'Value of $a ** $b = ' . ( $a**$b ) . "\n";     # 25
